/**
 * Provides utility functios for numbers.
 *
 * @module util/number
 */

/**
 * Tests a number if it is odd.
 *
 * @param {Number} n
 * @return {Boolean}
 */
export function isEven(n) {
  return n % 2 === 0;
}

/**
 * Tests a number if it is odd.
 *
 * @param {Number} n
 * @return {Boolean}
 */
export function isOdd(n) {
  return n % 2 === 1;
}
